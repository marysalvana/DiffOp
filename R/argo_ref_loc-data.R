#'
#' @author Mary Lai Salvana \email{yourlainess@gmail.com}
#'
#' @references Yarger, D., Stoev, S., & Hsing, T. (2022). A functional-data approach to the Argo data. \emph{The Annals of Applied Statistics, 16(1)}, 216-246.
#'
#' @source \url{https://github.com/dyarger/argofda}
#'
#' @keywords datasets
#'
#' @examples
#' data(argo_ref_loc1)
